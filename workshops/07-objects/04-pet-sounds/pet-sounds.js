function petSounds(ani, country) {
  let obj2 = {};
  for (i = 0; i < animalNoises.length; i++) {
    let arr = animalNoises[i];
    if (ani in arr) {
      let obj2 = arr[ani];
      let noise = obj2[country];
      return `${
        ani[0].toUpperCase() + ani.slice(1)
      }s in ${country} say ${noise}`;
    }
  }
}
